package alfred.command;

/**
 * Enumerates the different types of command supported
 * by the program.
 *
 * Unsupported commands are marked invalid.
 */
public enum CommandType {
    BYE,
    DATE,
    DEADLINE,
    DELETE,
    DONE,
    EVENT,
    FIND,
    HELP,
    INVALID,
    LIST,
    TODO,
}
