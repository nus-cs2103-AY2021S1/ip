package duke.task;

/**
 * The priority levels tasks can be assigned.
 */
public enum PriorityLevel {

    /**
     * Low priority.
     */
    LOW,

    /**
     * Medium priority.
     */
    MEDIUM,

    /**
     * High priority.
     */
    HIGH,

    /**
     * Undefined priority for when the task is first added.
     */
    UNDEFINED
}
