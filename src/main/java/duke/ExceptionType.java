package duke;

/**
 * This is the enum class to store the types of
 * exceptions Duke throws in the chatbot.
 */
public enum ExceptionType {
    EMPTY_INPUT,
    INVALID_COMMAND,
    EMPTY_DESCRIPTION,
    DEADLINE_NO_BY,
    EVENT_NO_START_END,
    EVENT_CLASHES_SCHEDULE,
    INDEX_OUT_OF_BOUNDS,
    DEFAULT
}
