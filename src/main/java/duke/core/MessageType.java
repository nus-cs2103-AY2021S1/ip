package duke.core;

/**
 * The MessageType class is an enumerator class and it describes
 * the different types of message and enumerates them.
 */
public enum MessageType {
    LOADING_MESSAGE,
    GREETING_MESSAGE,
    COMMAND_FOUND_MESSAGE,
    COMMAND_NOT_FOUND_MESSAGE,
    TASK_NOT_FOUND_MESSAGE,
    HANDLE_MESSAGE,
    USER_MESSAGE,
    HELP_MESSAGE;
}
